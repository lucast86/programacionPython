"""El programa debe calcula el área de un triángulo. El usuario debe ingresar cada una de las
medidas de sus lados."""

trianguloAltura = float(input("Ingrese la altura del angulo : "))
trianguloBase = float(input("Ingrese la base del angulo : "))

areaTriangulo = (trianguloAltura * trianguloBase) / 2
print(f"El area del trenagulo es : {areaTriangulo} ")